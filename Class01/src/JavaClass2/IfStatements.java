package JavaClass2;

public class IfStatements {

	public static void main(String[] args) {

		if(20 > 12)
		{
			System.out.println("print greater number 20");
		}

		if(12 < 21) {
			System.out.println("print smaller number 12");
		}

		if(20 <= 21)
		{
			System.out.println("print number less than or equal to 21");
		}

		if(20 >= 18)
		{
			System.out.println("print number greater than or equal to 18");
		}

		if(20 == 20)
		{
			System.out.println("print number equal to 20");
		}


		if(20 != 21)
		{
			System.out.println("print number not equal to 21");
		}


	}
}